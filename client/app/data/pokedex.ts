export function getPokedexByGen(gen: number): PokedexEntry[] {
  return POKEDEX.filter((p) => p.gen === gen)
}

export function getAllGens(): number[] {
  return [...new Set(POKEDEX.map((p) => p.gen))].sort((a, b) => a - b)
}
